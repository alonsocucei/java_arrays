
public class AgeAverage {

    public static void main(String[] args) {
        int age1 = 25;
        int age2 = 29;
        int age3 = 31;
        
        System.out.print("Age average for: ");
        System.out.print(age1);
        System.out.print(", ");
        System.out.print(age2);
        System.out.print(" and ");
        System.out.print(age3);
        System.out.print(" is: ");
        System.out.println((age1 + age2 + age3) / 3);
    }
}
