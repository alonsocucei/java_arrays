
public class ArraysDeclaration {

    public static void main(String[] args) {
        /*declaration per line - recommended*/
        int [] ages;
        int ageArray[];
        
        /*all declarations in same line - not recommended*/
        //all variables are arrays
        int [] myArray, yourArray, hisArray;
                
        //not all variables are arrays
        int ourArray [], theirArray;
    }
}
